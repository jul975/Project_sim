

'ϕW => selected world regime, combined with agent regime to define world specs'

phi_w = "world sup potential per cell / nominal agent demand per cell"
# defines world regime where: 
# - if ϕW < 1, then world is energy-scarce, and agents must compete for resources
# - if ϕW > 1, then world is energy-abundant, and agents can easily meet their energy needs
# - if ϕW = 1, then world is at a critical point where energy supply matches demand, and small changes can lead to large effects on agent behavior and population dynamics